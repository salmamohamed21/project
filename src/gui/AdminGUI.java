    private void loadStudentNamesIntoComboBox() {
    try {
        DatabaseOperations dbOps = new DatabaseOperations();
        List<String> studentNames = dbOps.getAllStudentNames();

        // Clear existing items
        jComboBox2.removeAllItems();

        // Add new items
        for (String name : studentNames) {
            jComboBox2.addItem(name);
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading student names: " + e.getMessage());
    }
}

    
    
    private void loadCoursesIntoComponent(Object component) {
    try {
        DatabaseOperations dbOps = new DatabaseOperations();
        List<String> courses = dbOps.getAllCoursesinlist(); // الحصول على جميع الكورسات من قاعدة البيانات

        if (component instanceof JComboBox) {
            JComboBox comboBox = (JComboBox) component;
            comboBox.removeAllItems(); // مسح العناصر الحالية
            for (String course : courses) {
                comboBox.addItem(course); // إضافة الكورسات إلى JComboBox
            }
        } else if (component instanceof JList) {
            JList list = (JList) component;
            DefaultListModel<String> listModel = new DefaultListModel<>();
            for (String course : courses) {
                listModel.addElement(course); // إضافة الكورسات إلى JList
            }
            list.setModel(listModel); // تحديث نموذج JList
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading courses: " + e.getMessage());
    }
}

private void loadCoursesWithEnrollment() {
    try {
        DatabaseOperations dbOps = new DatabaseOperations();
       
          String query = "  SELECT c.course_name, c.course_type, COUNT(e.student_id) AS student_count"+
            "FROM Courses c LEFT JOIN Enrollments e ON c.course_id = e.course_id "+
          "  GROUP BY c.course_name, c.course_type ";

        try (Connection conn = DatabaseConnection.getConnection();
             PreparedStatement stmt = conn.prepareStatement(query);
             ResultSet rs = stmt.executeQuery()) {

            // Create a DefaultTableModel with column headers
            DefaultTableModel model = new DefaultTableModel(
                new Object[]{"Course Name", "Course Type", "Student Count"}, 0
            );
            
            // Populate the model with data from the ResultSet
            while (rs.next()) {
                String courseName = rs.getString("course_name");
                String courseType = rs.getString("course_type");
                int studentCount = rs.getInt("student_count");

                model.addRow(new Object[]{courseName, courseType, studentCount});
            }
            
            // Set the model to jTable2
            jTable2.setModel(model);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading courses: " + e.getMessage());
    }
}

 
    
    
