    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
          // Retrieve selected course from jComboBox4
    String selectedCourse = (String) jComboBox4.getSelectedItem();
    if (selectedCourse == null || selectedCourse.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a course.");
        return;
    }

    try {
        DatabaseOperations dbOps = new DatabaseOperations();
        ResultSet rs = dbOps.getCourseDetails(selectedCourse); // Method to fetch course details

        if (rs.next()) {
            String courseName = rs.getString("course_name");
            String courseType = rs.getString("course_type");

            // Set retrieved values to the text field and combo box
            jTextField3.setText(courseName);
            jComboBox5.setSelectedItem(courseType);
        } else {
            JOptionPane.showMessageDialog(this, "Course not found in the database.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error retrieving course details: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        // Get modified course details from the components
    String updatedCourseName = jTextField3.getText();
    String updatedCourseType = (String) jComboBox5.getSelectedItem();
    String selectedCourse = (String) jComboBox4.getSelectedItem();

    if (updatedCourseName.isEmpty() || updatedCourseType.isEmpty() || selectedCourse == null || selectedCourse.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please ensure all fields are filled correctly.");
        return;
    }

    try {
        DatabaseOperations dbOps = new DatabaseOperations();
        boolean isUpdated = dbOps.updateCourse(selectedCourse, updatedCourseName, updatedCourseType); // Update method

        if (isUpdated) {
            JOptionPane.showMessageDialog(this, "Course updated successfully.");
            loadCoursesIntoComponent(jComboBox4); // Refresh the courses in jComboBox4
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update the course.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error updating course: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton8ActionPerformed

