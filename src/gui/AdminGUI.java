    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         // Get input values
    String studentName = jTextField1.getText(); // Get student name from input
    String studentType = (String) jComboBox1.getSelectedItem(); // Get selected student type
    List<String> courses = jList1.getSelectedValuesList(); // Get selected courses
    String username = studentName.toLowerCase().replace(" ", "_"); // Generate a username
    String password = "123"; // Default password for students

    // Using the StudentFactory to create a student
   // StudentFactory studentFactory = new StudentFactory();
    Student student = StudentFactory.createStudent(studentType, studentName);

    // Add student and enroll them in courses using DatabaseOperations
    DatabaseOperations dbOps = new DatabaseOperations();
    try {
        // Add login and retrieve userId
        int userId = dbOps.addLogin(username, password);
        if (userId == -1) {
            JOptionPane.showMessageDialog(this, "Failed to create login for student.");
            return;
        }

        // Add student to Students table
        dbOps.addStudent(student, userId);

        // Enroll student in selected courses
        for (String course : courses) {
            dbOps.enrollStudentInCourse(studentName, course);
        }

        JOptionPane.showMessageDialog(this, "Student added successfully!");
         loadStudentNamesIntoComboBox();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error adding student: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String studentName = (String) jComboBox2.getSelectedItem(); // Get selected student name
    DatabaseOperations dbOps = new DatabaseOperations();

    try {
        ResultSet rs = dbOps.getStudentDetails(studentName);
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear table
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("student_id"),
                rs.getString("name"),
                rs.getString("course_name"),
                rs.getString("grade")
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error fetching student details: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

